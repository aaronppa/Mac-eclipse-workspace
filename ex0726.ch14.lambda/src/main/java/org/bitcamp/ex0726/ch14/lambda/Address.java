package org.bitcamp.ex0726.ch14.lambda;

public class Address {

	private String country;
	private String city;
	
	public Address(String country, String city) {
		this.country = country;
		this.city = city;
		
	} // constructor

	public String getCountry() {
		return country;
	}
	
	public String getCity() {
		return city;
	}
	
	
} // end class
