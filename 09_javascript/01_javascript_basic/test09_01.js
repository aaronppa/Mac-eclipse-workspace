/*
    if, switch, for : 자바와 거의 동일함 
*/

"use strict"

var i = 100
if(i==100){
    console.log("i는 100")
} else {
    console.log("i는 100이 아님")
}

switch(i){
    case 10:
    console.log(10)
    break;
    case 100:
    console.log(100)
    break;
    default:
    console.log("default")
}

for( let i = 1; i <100; i++){
    console.log(i)
}

